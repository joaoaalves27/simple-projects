import time
import os
import pyfiglet

def clear_screen():
    os.system('cls' if os.name == 'nt' else 'clear')

def fade_text(text, fade_steps=10):
    for i in range(fade_steps):
        clear_screen()
        print(text)
        time.sleep(0.1)
    for i in range(fade_steps, 0, -1):
        clear_screen()
        print(text)
        time.sleep(0.1)

para_ascii = pyfiglet.figlet_format("MADE BY JOAO")
fade_text(para_ascii)

juanzito_ascii = pyfiglet.figlet_format("FEITO POR JOAO")
clear_screen()
print(juanzito_ascii)
fade_text(juanzito_ascii)

